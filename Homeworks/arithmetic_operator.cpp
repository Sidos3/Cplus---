#include <iostream>
using namespace std;

int main(){

short int A;
short int B;

cout << "Please enter number A" << endl;
cin >> A;

cout << "Please enter number B" << endl;
cin >> B ;

cout << A << " + " << B << " = " << A + B << endl;
cout << A << " - " << B << " = " << A - B << endl;
cout << A << " * " << B << " = " << A * B << endl;
cout << A << " / " << B << " = " << A / B << endl;
cout << A << " % " << B << " = " << A % B << endl;

return 0;

}