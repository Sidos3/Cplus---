#include <iostream>
using namespace std;






int main(){

    short int A, B;




    cout << "Please Enter The First Number" << endl;
    cin >> A;

    cout << "Please Enter The Second Number" << endl;
    cin >> B;

    cout << endl;

    cout << A << " = " << B << " is " << (A == B) << endl;
    cout << A << " != " << B << " is " << (A != B) << endl;
    cout << A << " > " << B << " is " << (A > B) << endl;
    cout << A << " < " << B << " is " << (A < B) << endl;
    cout << A << " >= " << B << " is " << (A >= B) << endl;
    cout << A << " <= " << B << " is " << (A <= B) <<  endl;




    return 0;
}